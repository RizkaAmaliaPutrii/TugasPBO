public class pinjaman {
    
    private String tanggalPinjaman;
    private String ketPinjaman;
    private String jatuhTempo;
    private String bulanBayar;
    
    public pinjaman(){
         
    }

    public String getTanggalPinjaman() {
        return tanggalPinjaman;
    }

    public void setTanggalPinjaman(String tanggalPinjaman) {
        this.tanggalPinjaman = tanggalPinjaman;
    }

    public String getKetPinjaman() {
        return ketPinjaman;
    }

    public void setKetPinjaman(String ketPinjaman) {
        this.ketPinjaman = ketPinjaman;
    }

    public String getJatuhTempo() {
        return jatuhTempo;
    }

    public void setJatuhTempo(String jatuhTempo) {
        this.jatuhTempo = jatuhTempo;
    }

    public String getBulanBayar() {
        return bulanBayar;
    }

    public void setBulanBayar(String bulanBayar) {
        this.bulanBayar = bulanBayar;
    }
    
}
