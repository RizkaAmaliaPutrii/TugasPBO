public class karyawan {
    
    private String idKaryawan;
    private String namaKaryawan;
    private String alamatKoperasi;
    private String telponKaryawan;
    private String emailKoperasi;
    private String statusKaryawan;
    
    public karyawan(){
        
    }

    public String getIdKaryawan() {
        return idKaryawan;
    }

    public void setIdKaryawan(String idKaryawan) {
        this.idKaryawan = idKaryawan;
    }

    public String getNamaKaryawan() {
        return namaKaryawan;
    }

    public void setNamaKaryawan(String namaKaryawan) {
        this.namaKaryawan = namaKaryawan;
    }

    public String getAlamatKoperasi() {
        return alamatKoperasi;
    }

    public void setAlamatKoperasi(String alamatKoperasi) {
        this.alamatKoperasi = alamatKoperasi;
    }

    public String getTelponKaryawan() {
        return telponKaryawan;
    }

    public void setTelponKaryawan(String telponKaryawan) {
        this.telponKaryawan = telponKaryawan;
    }

    public String getEmailKoperasi() {
        return emailKoperasi;
    }

    public void setEmailKoperasi(String emailKoperasi) {
        this.emailKoperasi = emailKoperasi;
    }

    public String getStatusKaryawan() {
        return statusKaryawan;
    }

    public void setStatusKaryawan(String statusKaryawan) {
        this.statusKaryawan = statusKaryawan;
    }
}
